class SwimmerData {
  String name = '';
  int index = 0;
  String url = '';
  String age = '';
  String club = '';
  String gender = '';

  SwimmerData(String name, int index, String url, String age, String club,
      String gender) {
    this.name = name;
    this.index = index;
    this.url = url;
    this.age = age;
    this.club = club;
    this.gender = gender;
  }
}
