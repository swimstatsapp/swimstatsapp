class SwimmerData {
  String name = '';
  int index = 0;
  String url = '';
  String age = '';
  String club = '';
  String gender = '';
  String lastMeet = '';
  String lastMeetDate = '';

  SwimmerData(String name, int index, String url, String age, String club,
      String gender, String lastMeet, String lastMeetDate) {
    this.name = name;
    this.index = index;
    this.url = url;
    this.age = age;
    this.club = club;
    this.gender = gender;
    this.lastMeet = lastMeet;
    this.lastMeetDate = lastMeetDate;
  }
}
